export function ThemeToggle() {
  const { theme, toggle } = useTheme();
  const isDark = theme === "dark";

  return (
    <Toggle.Root
      id="theme-toggle"
      pressed={isDark}
      onPressedChange={toggle}
      aria-label="Toggle theme"
    >
      {isDark ? <SunIcon /> : <MoonIcon />}
    </Toggle.Root>
  );
}
